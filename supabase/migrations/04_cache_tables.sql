CREATE TABLE regulatory_cache (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  brand_id uuid NOT NULL REFERENCES brands(id) ON DELETE CASCADE,
  scan_week date NOT NULL,
  competitor_id uuid NOT NULL REFERENCES competitors(id),
  market text NOT NULL,
  licence_display_status text CHECK (licence_display_status IN ('compliant','partial','non_compliant')),
  responsible_gambling_status text CHECK (responsible_gambling_status IN ('compliant','partial','non_compliant')),
  bonus_terms_status text CHECK (bonus_terms_status IN ('compliant','partial','non_compliant')),
  age_verification_status text CHECK (age_verification_status IN ('compliant','partial','non_compliant')),
  withdrawal_terms_status text CHECK (withdrawal_terms_status IN ('compliant','partial','non_compliant')),
  data_privacy_status text CHECK (data_privacy_status IN ('compliant','partial','non_compliant')),
  compliance_score numeric(5,2),
  violations jsonb DEFAULT '[]',
  raw_data jsonb,
  created_at timestamptz DEFAULT now(),
  UNIQUE(brand_id, scan_week, competitor_id, market)
);
