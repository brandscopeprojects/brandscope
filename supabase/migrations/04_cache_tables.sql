-- Section 4: Intelligence cache tables

CREATE TABLE weekly_cache (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  brand_id uuid NOT NULL REFERENCES brands(id) ON DELETE CASCADE,
  scan_week date NOT NULL,
  scan_job_id uuid REFERENCES scan_jobs(id),
  reach_score numeric(5,2),
  aggression_score numeric(5,2),
  threat_score numeric(5,2),
  sov_pct numeric(5,2),
  threat_level text CHECK (threat_level IN ('low','medium','high','critical')),
  threat_reasons text[],
  ai_visibility_score numeric(5,2),
  ai_visibility_trend numeric(5,2),
  competitors_tracked integer,
  promo_changes_this_week integer,
  active_ads_count integer,
  promotions_data jsonb,
  traffic_seo_data jsonb,
  social_data jsonb,
  ads_data jsonb,
  tech_stack_data jsonb,
  product_data jsonb,
  customer_data jsonb,
  regulatory_data jsonb,
  hiring_data jsonb,
  geo_aeo_data jsonb,
  radar_data jsonb,
  competitor_states jsonb,
  cached_at timestamptz DEFAULT now(),
  expires_at timestamptz,
  created_at timestamptz DEFAULT now(),
  updated_at timestamptz DEFAULT now(),
  UNIQUE(brand_id, scan_week)
);

